package do_while;

public class Exercise01 {

	public static void main(String[] args) {
		
		//Declaramos una variable para contener un numero
		int numero = 1;
		
		//Usamos Do para mostrar los 20 primero numeros naturales
		do {
			System.out.println(numero);
			numero++;
		} while(numero <= 20);
		
	}

}
